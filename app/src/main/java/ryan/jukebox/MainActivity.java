    private ArrayList<SongInfo> queueData = new ArrayList<SongInfo>();

    /**
     * A {@link FragmentPagerAdapter} that returns a fragment corresponding to
     * one of the sections/tabs/pages.
     */
    public class SectionsPagerAdapter extends FragmentPagerAdapter {

        public SectionsPagerAdapter(FragmentManager fm) {
            super(fm);
        }

        @Override
        public Fragment getItem(int position) {
            // getItem is called to instantiate the fragment for the given page.
            // Return a PlaceholderFragment (defined as a static inner class below).
            switch (position) {
                case 0:
                    final PlayerFragment pf = new PlayerFragment();

                    new Timer().schedule(new TimerTask() {
                        @Override
                        public void run() {
                            runOnUiThread(new Runnable() {
                                @Override
                                public void run() {
                                    pf.setTrackInfo(JukeboxAPI.getTrackInfo());
                                }
                            });
                        }
                    }, 0, 1000);

                    return pf;
                case 1:
                    final QueueFragment qf = new QueueFragment();
                    qf.setQueueData(queueData);

                    new Timer().schedule(new TimerTask() {
                        @Override
                        public void run() {
                            runOnUiThread(new Runnable() {
                                @Override
                                public void run() {
                                    queueData.clear();
                                    queueData.addAll(Arrays.asList(JukeboxAPI.getQueue()));

                                    qf.setQueueData(queueData);
                                }
                            });
                        }
                    }, 0, 1000);

                    return qf;
                case 2:
                    return new SearchFragment();
            }

            return PlaceholderFragment.newInstance(position + 1);
        }

        @Override
        public int getCount() {
            return 3;
        }

        @Override
        public CharSequence getPageTitle(int position) {
            switch (position) {
                case 0:
                    return "Player";
                case 1:
                    return "Queue";
                case 2:
                    return "Search";
            }
            return null;
        }
    }
